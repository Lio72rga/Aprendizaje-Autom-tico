#Cálculo del cuadrado de un número
#Escribe un programa que solicite un número al usuario, 
# calcule su cuadrado y muestre el resultado.
# Solicitar el número al usuario
numero = float(input("Por favor, ingresa un número: "))

# Calcular el cuadrado del número
cuadrado = numero ** 2

# Mostrar el resultado
print(f"El cuadrado de {numero} es {cuadrado}.")