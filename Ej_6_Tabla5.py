#Escribe un programa que imprima la tabla de multiplicar del 
# número 5 (del 1 al 10)
# Imprimir la tabla de multiplicar del 5
print("Tabla de multiplicar del 5:")

# Usar un bucle for para generar la tabla del 5 del 1 al 10
for i in range(1, 11):
    resultado = 5 * i
    print(f"5 x {i} = {resultado}")